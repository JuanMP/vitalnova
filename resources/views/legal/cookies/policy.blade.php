    <h5>¿Qué tipo de cookies utilizamos?</h5>
    <p>Según la entidad que las gestione, las cookies pueden ser:</p>
    <ul class="collection">
        <li class="collection-item">
            <strong>Cookies propias:</strong> Son aquellas que se envían al equipo terminal del interesado desde un equipo o dominio gestionado por el propio editor y desde el que se presta el servicio solicitado por el usuario.
        </li>
        <li class="collection-item">
            <strong>Cookies de tercero:</strong> Son aquellas que se envían al equipo terminal del usuario desde un equipo o dominio que no es gestionado por el editor, sino por otra entidad que trata los datos obtenidos través de las cookies.
        </li>
    </ul>
    <p>El Sitio Web utiliza tanto cookies propias como de terceros.</p>
    <p>Según el plazo de tiempo que permanecen activadas:</p>
    <ul class="collection">
        <li class="collection-item">
            <strong>Cookies de sesión:</strong> Son un tipo de cookies diseñadas para recabar y almacenar datos mientras el usuario accede a una página web. Se suelen emplear para almacenar información que solo interesa conservar para la prestación del servicio solicitado por el usuario en una sola ocasión (p.ej. una lista de productos adquiridos).
        </li>
        <li class="collection-item">
            <strong>Cookies persistentes:</strong> Son un tipo de cookies en el que los datos siguen almacenados en el terminal y pueden ser accedidos y tratados durante un periodo definido por el responsable de la cookie, y que puede ir de unos minutos a varios años.
        </li>
    </ul>
    <p>El Sitio Web utiliza tanto cookies persistentes como cookies de sesión.</p>

    <h5>¿Para qué finalidades pueden ser utilizadas las cookies del Sitio web?</h5>
    <ul class="collection">
        <li class="collection-item">
            <strong>Cookies técnicas:</strong> Son aquéllas que permiten al usuario la navegación a través de una página web, plataforma o aplicación y la utilización de las diferentes opciones o servicios que en ella existan como, por ejemplo, controlar el tráfico y la comunicación de datos, identificar la sesión, acceder a partes de acceso restringido, recordar los elementos que integran un pedido, realizar el proceso de compra de un pedido, realizar la solicitud de inscripción o participación en un evento, utilizar elementos de seguridad durante la navegación, almacenar contenidos para la difusión de videos o sonido o compartir contenidos a través de redes sociales.
        </li>
        <li class="collection-item">
            <strong>Cookies de personalización:</strong> Son aquéllas que permiten al usuario acceder al servicio con algunas características de carácter general predefinidas en función de una serie de criterios en el terminal del usuario como por ejemplo serian el idioma, el tipo de navegador a través del cual accede al servicio, la configuración regional desde donde accede al servicio, etc.
        </li>
        <li class="collection-item">
            <strong>Cookies de análisis:</strong> Son aquéllas que permiten al responsable de las mismas, el seguimiento y análisis del comportamiento de los usuarios de los sitios web a los que están vinculadas. La información recogida mediante este tipo de cookies se utiliza en la medición de la actividad de los sitios web, aplicación o plataforma y para la elaboración de perfiles de navegación de los usuarios de dichos sitios, aplicaciones y plataformas, con el fin de introducir mejoras en función del análisis de los datos de uso que hacen los usuarios del servicio.
        </li>
        <li class="collection-item">
            <strong>Cookies publicitarias:</strong> Son aquéllas que permiten la gestión, de la forma más eficaz posible, de los espacios publicitarios que, en su caso, el editor haya incluido en una página web, aplicación o plataforma desde la que presta el servicio solicitado en base a criterios como el contenido editado o la frecuencia en la que se muestran los anuncios.
        </li>
        <li class="collection-item">
            <strong>Cookies de publicidad comportamental:</strong> Son aquéllas que permiten la gestión, de la forma más eficaz posible, de los espacios publicitarios que, en su caso, el editor haya incluido en una página web, aplicación o plataforma desde la que presta el servicio solicitado. Estas cookies almacenan información del comportamiento de los usuarios obtenida a través de la observación continuada de sus hábitos de navegación, lo que permite desarrollar un perfil específico para mostrar publicidad en función del mismo.
        </li>
    </ul>
